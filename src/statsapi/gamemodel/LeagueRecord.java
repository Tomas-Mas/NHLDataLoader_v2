package statsapi.gamemodel;

public class LeagueRecord {

	private String wins;
	private String losses;
	private String ot;
	private String type;
	
	public String getWins() {
		return wins;
	}
	
	public String getLosses() {
		return losses;
	}
	
	public String getOt() {
		return ot;
	}
	
	public String getType() {
		return type;
	}
}
